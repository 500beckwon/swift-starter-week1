import Foundation

func printIcecream(icecreamSize: Int) {
    for _ in 1...icecreamSize {
        print("***********")
    }
}
func printIcecreamStick(stickSize: Int) {
    for _ in 1...stickSize {
        print("    | |")
    }
}
