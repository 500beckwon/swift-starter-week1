import Foundation

func drawIcecream(rowI : Int) {
    for count in 1...rowI{
        print("***********")}
}
func drawBar(rowB : Int){
    for count in 1...rowB{
        print("    | |")}
}

drawIcecream(rowI: 8)

drawBar(rowB: 4)
