import Foundation

func icecream(x: Int) {
    for _ in 1...x {
        for _ in 1...11{
            print("*",terminator: "")
            }
        print()
}
}
func stick(y: Int) {
    for _ in 1...y{
        print("   ","|","|")
    }
}

icecream(x:8)
stick(y:4)
