import Foundation

func drawIcecream(rowIcecream : Int) {
    for count in 1...rowIcecream{
        print("***********")}
}
func drawBar(rowBar : Int) {
    for count in 1...rowBar{
        print("    | |")}
}

drawIcecream(rowIcecream: 8)

drawBar(rowBar: 4)
