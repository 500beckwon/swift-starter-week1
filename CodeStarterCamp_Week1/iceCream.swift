import Foundation

/* step 2 코드
printIce(lengthOfIce:8)
printBar(lengthOfBar:4)
*/

func printIce(lengthOfIce : Int) {
    for _ in 1...lengthOfIce{
        print("***********")
    }
}

func printBar(lengthOfBar : Int) {
    for _ in 1...lengthOfBar{
        print("    | |")
    }
}

