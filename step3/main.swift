// 1 빼뺴로 스틱 : 길이에 따른 막대(빼빼로손잡이)를 그리는 함수
// 1-1 함수선언
func printPeperoStick(막대길이 : Int){
    for _ in 1...막대길이 {
        print (" | | ")
    }
}

// 1-2 함수호출
print("1. 빼빼로 스틱 길이")
printPeperoStick(막대길이: 8)

/*----------------------------------------------------*/ print ("    ")

// 2 빼빼로 몸통과 토핑을 매개변수로 받아 토핑과 몸통을 그리는 함수
// 2-1 함수선언
func printPeperoBodyComponent(토핑: String, 몸통: String){
        print ("\(토핑)\(몸통)\(토핑)")
