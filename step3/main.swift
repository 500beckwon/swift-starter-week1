// 4 각종 매개변수를 받아 위의 세 메서드를 호출하는 함수
// 4-1 함수선언
func printPepero (몸통길이: Int, 토핑: String, 몸통: String, 막대길이: Int){
    
    print("<정보> \n길이:\(몸통길이) \n토핑:\(토핑) \n몸통:\(몸통) \n막대길이:\(막대길이) \n ")
    
    for _ in 1...몸통길이{
        print("\(토핑)\(몸통)\(토핑)")
    }
    
    for _ in 1...막대길이{
        print(" | | ")
    }
}
 // 4-2 함수호출
print("4.나만의 빼빼로만들기")
printPepero(몸통길이: 6, 토핑: "-", 몸통: "|8|", 막대길이: 2)








